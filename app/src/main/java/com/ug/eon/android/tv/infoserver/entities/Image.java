public class Image {
    private String path;
    private String size;
    private String type;
    private String mode;
    private int width;
    private int height;

    public String getPath() { return path; }
    public String getSize() { return size; }
    public String getType() { return type; }
    public String getMode() { return mode; }
    public int getWidth() { return width; }
    public int getHeight() { return height; }
}
